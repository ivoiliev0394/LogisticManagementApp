        /// <summary>
        /// Взима CompanyId от claims на логнатия потребител.
        /// </summary>
        private async Task<Guid?> GetCurrentCompanyIdAsync()
        {
            var user = await _userManager.GetUserAsync(User);
            return user?.CompanyId;
        }

        public override async Task OnActionExecutionAsync(ActionExecutingContext context, ActionExecutionDelegate next)
        {
            var companyId = await GetCurrentCompanyIdAsync();
            var actionName = context.ActionDescriptor.RouteValues.TryGetValue("action", out var value) ? value : null;
            var isEditAction = actionName != null && actionName.StartsWith("Edit", StringComparison.OrdinalIgnoreCase);

            if (companyId != null)
            {
                foreach (var argument in context.ActionArguments.Values)
                {
                    if (argument != null)
                    {
                        await _companyPortalService.PrepareAutoGeneratedFieldsAsync(companyId.Value, argument, isEditAction);
                    }
                }
            }

            var executedContext = await next();

            if (companyId != null && executedContext.Result is ViewResult viewResult && viewResult.Model != null)
            {
                await _companyPortalService.PrepareAutoGeneratedFieldsAsync(companyId.Value, viewResult.Model, isEditAction);
            }
        }

